//===----------------------------------------------------------------------===//
// SHUXITargetAsmStreamer
//===----------------------------------------------------------------------===//

SHUXITargetAsmStreamer::SHUXITargetAsmStreamer(MCStreamer &S,
                                                 formatted_raw_ostream &OS)
    : SHUXITargetStreamer(S), OS(OS)
{
}

void SHUXITargetAsmStreamer::EmitStartOfAsmFile(Module &M)
{
  OS << "//\n";
  OS << "// Generated by LLVM SHUXI Back-End\n";
  OS << "//\n";
  OS << "\n";
}

//===----------------------------------------------------------------------===//
// SHUXITargetELFStreamer
//===----------------------------------------------------------------------===//

SHUXITargetELFStreamer::SHUXITargetELFStreamer(MCStreamer &S)
    : SHUXITargetStreamer(S), Streamer(S)
{
}

MCELFStreamer &SHUXITargetELFStreamer::getStreamer()
{
  return static_cast<MCELFStreamer &>(Streamer);
}

